% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%tell that a and b texfield handles a string
a = get(handles.a, 'string');
b = get(handles.b, 'string');

% the textfield wants a string but the answer is a number so num2str
% converts a number to string and str2num converts a string to number.
c = num2str(str2num(a)+str2num(b))

%set.
set(handles.c, 'string', c);



